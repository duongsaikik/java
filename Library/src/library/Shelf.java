 private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"ID tủ sách","Tầng sách"});//set danh sách cột trong table
        Shelftable.setModel(table);
    }
 public void loadData()
    {
        try {
            ShelfDAO sf=new  ShelfDAO();
            List<Model.Shelf> list=sf.FindShelf();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Shelf s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getTang()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }               

