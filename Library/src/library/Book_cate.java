    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        StringBuilder sb=new StringBuilder();
        Validator.CheckText(txtcate, sb, "Chưa nhập tên danh mục");
         if(sb.length()>0)
            {
                Notify.ChickenMessageDialog(this, sb.toString(), "Warning");
                return;
            }
        try {
          Model.Book_cate s=new Model.Book_cate();
          s.setCateName(txtcate.getText());
          BookCateDAO st=new BookCateDAO();
          if(st.insertBookCate(s))
          {
              Notify.ChickenMessageDialog(this, "Thêm danh mục sách thành công", "Thông báo");
              loadData();
          }
          else{
              Notify.ChickenErrorMessageDialog(this, "Đã xảy ra lỗi vui lòng xem lại", "Cảnh báo");
          }
        } catch (Exception e) {
            e.printStackTrace();//in ra chi tiết lỗi
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnaddActionPerformed

private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"Mã danh mục sách","Tên danh mục sách"});//set danh sách cột trong table
        tableCategory.setModel(table);
    }
 public void loadData()
    {
        try {
            BookCateDAO sf=new  BookCateDAO();
            List<Model.Book_cate> list=sf.FindBookCate();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Book_cate s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getCateName()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }
