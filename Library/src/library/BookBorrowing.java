    private void btnchomuonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchomuonActionPerformed
        // TODO add your handling code here:
         StringBuilder sb=new StringBuilder();
        Validator.CheckText(txtday, sb, "Ngày rỗng");
        Validator.CheckText(txtisbn, sb, "Mã sách rỗng");
        Validator.CheckText(txtreaderid, sb, "Mã đọc giả rỗng");
        if(sb.length()>0)
        {
            Notify.ChickenMessageDialog(this, sb.toString(), "Lỗi");
            return;
        }
        try {
            Model.Borrwing s=new Model.Borrwing();
            BorrwingDAO data=new BorrwingDAO();
            s.setReaderID(txtreaderid.getText());
            s.setISBN(txtisbn.getText());
            s.setHsd(txtday.getText());
            if(data.insertMuon(s))
            {
                Notify.ChickenMessageDialog(this, "Thêm danh sách mượn thành công", "Thông báo");
                loadData();
            }
            else{
                Notify.ChickenErrorMessageDialog(this, "Đã xảy ra lỗi vui lòng xem lại", "Cảnh báo");
            }
        } catch (Exception e) {
            e.printStackTrace();//in ra chi tiết lỗi
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnchomuonActionPerformed

    private void btntrasachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntrasachActionPerformed
        // TODO add your handling code here:
       StringBuilder sb=new StringBuilder();
        if(id==null)
        {
            sb.append("Chưa chọn đọc giả để trả sách");

        }
        if(sb.length()>0)
        {
            Notify.ChickenMessageDialog(this, sb.toString(), "Báo lỗi");
            return;
        }
        if(Notify.ChickenConfirmDialog(this, "Bạn có muốn thanh toán không?", "Thông báo")==JOptionPane.NO_OPTION)
        {
            return;
        }
        try {
            BorrwingDAO sf=new BorrwingDAO();
            Model.Borrwing s=new Model.Borrwing();
            s.setId(id);
            if(sf.payment(s))
            {
                Notify.ChickenMessageDialog(this, "Đã trả sách thành công", "Thông báo");
                loadData();
            }
            else{
                Notify.ChickenMessageDialog(this, "Lỗi vui lòng xem lại", "Báo lỗi");
            }
        } catch (Exception e) {
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btntrasachActionPerformed
