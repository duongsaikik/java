private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"ID","Mã đọc giả","Mã sách","Ngày mượn","Mượn đến","Ngày trả","Trễ hạn","Chi phí","Trạng thái"});//set danh sách cột trong table
        tableborrwing.setModel(table);
    }
 public void loadData()
    {
        try {
            BorrwingDAO sf=new  BorrwingDAO();
            List<Model.Borrwing> list=sf.FindBorrowing();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Borrwing s:list)
            {
                table.addRow(new Object[]{
                   s.getId(),s.getReaderID(),s.getISBN(),s.getNgaymuon(),s.getHsd(),s.getNgaytra(),s.getTrehan(),s.getChiphi(),s.getStatus()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }
