private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"ID","ISBN","Mã nhà văn","Mã tủ sách","Mã ngăn kệ của tủ","Tầng sách","Số lượng mượn","SL sách trong kho","Trạng thái"});//set danh sách cột trong table
        tableBookStatus.setModel(table);
    }
 public void loadData()
    {
        try {
            BookStatusDAO sf=new  BookStatusDAO();
            List<Model.BookStatus> list=sf.FindBookStatus();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.BookStatus s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getISBN(),s.getWriterID(),s.getShelfID(),s.getShelfNO(),s.getFloorNO(),s.getBorrowingCount(),s.getInstockMount(),s.getBookStatus()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }     
    
      
