    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
         StringBuilder sb=new StringBuilder();
        Validator.CheckText(txtname, sb, "Tên sách rỗng");
        Validator.CheckText(txtIsbn, sb, "Mã sách rỗng");
        Validator.CheckText(txtdayadd, sb, "Ngày thêm rỗng");
         if(sb.length()>0)
            {
                Notify.ChickenMessageDialog(this, sb.toString(), "Lỗi");
                return;
            }
         if(Notify.ChickenConfirmDialog(this, "Bạn có muốn cập nhập không?", "Thông báo")==JOptionPane.NO_OPTION)
         {
             return;
         }
        try {
          BookDAO data=new BookDAO();
          Model.Book s=new Model.Book();
          s.setId(id);
          Model.Book_cate get1=data.FindIDcatebook(cbBookCate.getSelectedItem().toString());
          Writer get2=data.FindIDcatewriter(cbWriter.getSelectedItem().toString());
          Publisher get3=data.FindIDcatepublisher(cbPub.getSelectedItem().toString());
          s.setISBN(txtIsbn.getText());
          s.setBookName(txtname.getText());
          s.setBookCateID(get1.getId());
          s.setPubID(get3.getId());
          s.setWriterID(get2.getId());
          s.setPublisherYear(cbYear.getSelectedItem().toString());
          s.setDateAdd(txtdayadd.getText());
          s.setHinh(person);
          if(data.updateBook(s))
          {
              Notify.ChickenMessageDialog(this, "Cập nhập dữ liệu thành công", "Thông báo");
              loadData();
          }
          else{
              Notify.ChickenErrorMessageDialog(this, "Đã xảy ra lỗi vui lòng xem lại", "Cảnh báo");
          }
        } catch (Exception e) {
            e.printStackTrace();//in ra chi tiết lỗi
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

