private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"Mã danh mục NXB","Tên danh mục NXB"});//set danh sách cột trong table
        tablePub.setModel(table);
    }
 public void loadData()
    {
        try {
            PublisherDAO sf=new  PublisherDAO();
            List<Model.Publisher> list=sf.FindPublisher();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Publisher s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getPubname()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }

