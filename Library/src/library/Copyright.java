    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
         StringBuilder sb=new StringBuilder();
        Validator.CheckText(txtisbn, sb, "Chưa nhập mã");
        Validator.CheckText(txtprice, sb, "Chưa nhập chi phí");
         if(sb.length()>0)
            {
                Notify.ChickenMessageDialog(this, sb.toString(), "Warning");
                return;
            }
        try {
          Model.Copyright s=new Model.Copyright();
          CopyrightDAO st=new CopyrightDAO();
          Model.Writer w=st.FinById(cbwriter.getSelectedItem().toString());
          s.setIsbn(txtisbn.getText());
          s.setWriter(w.getId());
          s.setChiphi(txtprice.getText());
          if(st.insertCopy(s))
          {
             Notify.ChickenMessageDialog(this, "Thêm bản quyền thành công", "Thông báo");
              loadData();
          }
          else{
              
               Notify.ChickenErrorMessageDialog(this, "Đã xảy ra lỗi vui lòng xem lại", "Cảnh báo");
          }
        } catch (Exception e) {
            e.printStackTrace();//in ra chi tiết lỗi
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnthemActionPerformed

