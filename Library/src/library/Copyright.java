    public void loadWriter()
    {
        try{
            WriterDAO vio=new WriterDAO();
            List<Model.Writer> list=vio.FindWriter();
            for(Model.Writer v:list)
            {
                cbwriter.addItem(v.getName());
            }
        }catch(Exception e)
        {
            
        }
    }
    private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"ID","Ngày","Mã bản quyền","Mã nhà văn","Chi phí"});//set danh sách cột trong table
        tableCopyright.setModel(table);
    }
 public void loadData()
    {
        try {
            CopyrightDAO sf=new  CopyrightDAO();
            List<Model.Copyright> list=sf.FindCopy();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Copyright s:list)
            {
                table.addRow(new Object[]{
                   s.getId(),s.getDay(),s.getIsbn(),s.getWriter(),s.getChiphi()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }
