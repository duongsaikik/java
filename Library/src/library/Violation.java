    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        StringBuilder sb=new StringBuilder();
        Validator.CheckText(txtvio, sb, "Chưa nhập tên loại vi phạm");
         if(sb.length()>0)
            {
                Notify.ChickenMessageDialog(this, sb.toString(), "Warning");
                return;
            }
        try {
          Model.Violation s=new Model.Violation();
          s.setName(txtvio.getText());
          ViolationDAO st=new ViolationDAO();
          if(st.insertViolation(s))
          {
              Notify.ChickenMessageDialog(this, "Thêm tên loại vi phạm thành công", "Thông báo");
              loadData();
          }
          else{
              Notify.ChickenErrorMessageDialog(this, "Đã xảy ra lỗi vui lòng xem lại", "Cảnh báo");
          }
        } catch (Exception e) {
            e.printStackTrace();//in ra chi tiết lỗi
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }                                      
private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"Mã vi phạm","Tên loại vi phạm"});//set danh sách cột trong table
        tablevio.setModel(table);
    }
 public void loadData()
    {
        try {
            ViolationDAO sf=new  ViolationDAO();
            List<Model.Violation> list=sf.FindViolation();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Violation s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getName()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnaddActionPerformed


