private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"Mã Thống Kê","Thống Kê Theo Ngày","Doanh Thu"});//set danh sách cột trong table
        tableRevenue.setModel(table);
    }
 public void loadData()
    {
        try {
            RevenueDAO sf=new  RevenueDAO();
            List<Model.Revenue> list=sf.FindRevenue();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Revenue s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getDate(),s.getGain()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }
