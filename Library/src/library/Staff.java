    private void initTable()
    {
        table=new DefaultTableModel();
        table.setColumnIdentifiers(new String[]{"Mã nhân viên","Tài khoản","Mật khẩu","Phân quyền","Chỉ định"});//set danh sách cột trong table
        tablestaff.setModel(table);
    }
    public void loadData()
    {
        try {
            StaffDAO sf=new StaffDAO();
            List<Model.Staff> list=sf.FindAll();
            table.setRowCount(0);//trc khi cap nhâp xoa hết data r update lại
            for(Model.Staff s:list)
            {
                table.addRow(new Object[]{
                    s.getId(),s.getUser(),s.getPass(),s.getAdmin(),s.getDesignation()
                });
            }
            table.fireTableDataChanged();//cập nhập hiển thị dữ liệu
        } catch (Exception e) {
        }
    }
    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        StringBuilder sb=new StringBuilder();
        if(txtcode.getText().equals(""))
        {
            sb.append("Chưa nhập mã nhân viên");
            txtcode.setBackground(Color.red);
        }
        else{
            txtcode.setBackground(Color.WHITE);
        }
        if(sb.length()>0)
        {
            Notify.ChickenMessageDialog(this, sb.toString(), "Báo lỗi");
            return;
        }
        try {
            StaffDAO sf=new StaffDAO();
            Model.Staff s=new Model.Staff();
            s.setId(txtcode.getText().trim());
            if(sf.deleteStaff(s))
            {
                Notify.ChickenMessageDialog(this, "Xóa dữ liệu có mã "+txtcode.getText()+" thành công", "Thông báo");
                loadData();
            }
            else{
                Notify.ChickenMessageDialog(this, "Không có mã "+txtcode.getText()+" trong CSDL", "Báo lỗi");
            }
        } catch (Exception e) {
            Notify.ChickenErrorMessageDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

